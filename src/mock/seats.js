export const seatsDivider = (seats = [], width = 8, height = 10) => {
  let result = [];
  for (let h = 0; h < height; h++) {
    let tempRow = [];
    for (let w = 0; w < width; w++) {
      tempRow.push(seats[h*width + w])
    }
    result.push(tempRow)
  }
  return result;
}

export const seatsMerger = (seats) => {
  let result = [];
  seats.map(item => {
    item.map(item2 => {
      result.push(item2)
    })
  })
  return result;
}

export const seatsChoosenGetter = (seats) => {
  let newSeats = seatsMerger(seats);
  let result = newSeats.filter(item => item.choosen)
  return result;
}

